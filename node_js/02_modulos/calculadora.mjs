const sumar = (a, b) => {
    return a + b;
  },
  restar = (a, b) => {
    return a - b;
  },
  dividir = (a, b) => {
    return a / b;
  },
  multiplicar = (a, b) => {
    return a * b;
  },
  modulo = (a, b) => {
    return a % b;
  };

const calculadora = {
  sumar,
  restar,
  dividir,
  multiplicar,
  modulo,
};
export default calculadora;
